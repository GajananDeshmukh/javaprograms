
package demo.decisionmaking;
public class Switchdemo {
    int choice=5;
    void switchdemo(){
        switch (choice){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break; 
            case 3:
                System.out.println("wednesday ");
                break;
            default :
                System.out.println("number  is invalid ");
            
        }
        
    }
}
