//row=5 column=ith value print =j
package demo.loops;
public class Fifthforloop {
    public static void main(String[] args) {
          {
        int r=5;
     
        
        for(int i=1;i<=r;i++){
             for(int j=1;j<=i;j++){
                 System.out.print(j);
             } 
             System.out.println();
        }
        
    }
    }
}
      