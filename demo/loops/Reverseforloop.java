
package demo.loops;
public class Reverseforloop {
    public static void main(String[] args) {
          {
        int r=5;
        int c=5;
        
        for(int i=1;i<=r;i++){
             for(int j=1;j<=c;j++){
                 System.out.print(j + " ");
              //--c;  
             } 
             System.out.println();
              --c;
             
        }
        
        
    }
    }
}
 
