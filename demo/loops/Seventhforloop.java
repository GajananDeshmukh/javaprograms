
package demo.loops;
public class Seventhforloop {
    public static void main(String[] args) {
          {
        int r=4;
        int c=10;
        
        for(int i=1;i<=r;i++){
             for(int j=1;j<=i;j++){
                 System.out.print(c + " ");
                 c--;
             } 
             System.out.println();
        }
        
    }
    }
}
   