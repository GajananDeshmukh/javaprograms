
public class Multideimensionalarray {
    public static void main(String[] args) {
        int arr[][]={{2,7,9},{4,8,9},{10,23,5}};
        for(int i=0;i<3;i++){
              for(int j=0;j<3;j++){
                  System.out.print(arr[i][j] + " ");
              }
                System.out.println();
        }
    }
}
